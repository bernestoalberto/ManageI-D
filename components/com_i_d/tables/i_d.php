 	function loadAllwithParent($key=null,$where=null)
    {
        $db=$this->getDbo();
        $query=$db->getQuery(true);
        $query->select($key);
        if(!is_null($where) && !empty($where))
            $query->where($where);
        $query->from($this->getTableName());
        $query->rightJoin('#__proyecto on (#__i_d.id_proyecto = #__proyecto.id_proyecto)');
        $query->rightJoin('#__proyecto on (#__i_d.id_persona = #__proyecto.id_persona)');
        $query->order('#__proyecto.id_persona');
        $db->escape($query);
